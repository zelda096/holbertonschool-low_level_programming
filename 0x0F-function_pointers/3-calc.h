#ifndef CALCULATOR_OPERATOR
#define CALCULATOR_OPERATOR
/**
 * struct op - structure for operator
 * @op: The operator
 * @f: The function associated
 */

typedef struct op
{
	char *op;
	int (*f)(int a, int b);
} op_t;

int op_add(int a, int b);

int op_sub(int a, int b);

int op_mul(int a, int b);

int op_div(int a, int b);

int op_mod(int a, int b);

#endif /**calculator*/
